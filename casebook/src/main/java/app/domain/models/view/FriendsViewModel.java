package app.domain.models.view;

public class FriendsViewModel {

    String id;
    String username;

    public FriendsViewModel() {

    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

}
